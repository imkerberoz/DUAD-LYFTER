        # FOLLOWING THE ANALOGY OF A SHAPE FACTORY, WE INSTANTIATE DIFFERENT SHAPES WITH SPECIFIC DIMENSIONS
        circle = Circle(5)      # CIRCLE WITH RADIUS 5
        square = Square(4)      # SQUARE WITH SIDE 4
        rectangle = Rectangle(6, 3)  # RECTANGLE 6×3
        
        
        print("Circle (radius = 5):") #HERE WE ARE PRINTING THE TYPE OF SHAPE AND ITS DIMENSIONS
        print(circle.display_info()) #HERE WE ARE CALLING THE DISPLAY METHOD FOR EACH SHAPE TO SHOW THEIR AREA AND PERIMETER
        print()  # THIS LINE IS FOR AESTHETIC PURPOSES, TO ADD A BLANK LINE BETWEEN OUTPUTS FOR BETTER READABILITY.
        
