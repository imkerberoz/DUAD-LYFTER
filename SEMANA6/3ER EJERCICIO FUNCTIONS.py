#CREATE A FUNCTION THAT RETURNS THE SUM OF ALL NUMBERS IN A LIST.

def sum_of_list(my_numbers = [1, 2, 3, 4, 5]  ):
    my_numbers = sum(my_numbers)  # USE THE BUILT-IN sum() FUNCTION TO CALCULATE THE SUM
    return my_numbers  # RETURN THE SUM

print(f"The sum of the list is: {sum_of_list()}")  # CALL THE FUNCTION AND PRINT THE RESULT





