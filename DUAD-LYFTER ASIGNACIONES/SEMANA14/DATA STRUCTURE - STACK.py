print("CREATING A NEW EMPTY STACK...\n")
pile = Stack() # THIS IS HOW WE CREATE A NEW STACK, IT STARTS EMPTY WITH TOP = NONE

#NEXT LINE IS HOW WE CALL THE PRINT FUNCTION TO SEE WHAT IS IN THE STACK, SINCE WE JUST CREATED IT AND DIDN'T ADD ANY ELEMENTS, IT SHOULD SHOW A MESSAGE INDICATING THAT THE STACK IS EMPTY
pile.print_stack() # THIS SHOULD SHOW A MESSAGE INDICATING THAT THE STACK IS EMPTY, BECAUSE WE JUST CREATED IT AND NO ELEMENTS HAVE BEEN ADDED YET

print("\nPUSHING SOME NUMBERS...\n")
pile.push(10) # THIS IS HOW WE ADD ELEMENTS TO THE STACK, WE CALL THE PUSH FUNCTION WITH THE VALUE WE WANT TO ADD, IN THIS CASE WE ARE ADDING 10, THEN 20, THEN 35, THEN 99, EACH TIME WE CALL PUSH, A NEW NODE IS CREATED AND ADDED TO THE TOP OF THE STACK    
pile.push(20) #THIS WOULD BE THE SECOND ELEMENT IN THE STACK, BUT IT WILL BE BELOW 10 BECAUSE 10 WAS ADDED FIRST, REMEMBER THIS IS A LIFO STRUCTURE, SO THE LAST ONE ADDED (20) WILL BE ON TOP OF THE ONE ADDED BEFORE (10)
pile.push(35) #THIS WOULD BE THE THIRD ELEMENT IN THE STACK, BUT IT WILL BE BELOW 20 AND 10 BECAUSE 20 WAS ADDED BEFORE AND 10 WAS ADDED FIRST, REMEMBER THIS IS A LIFO STRUCTURE, SO THE LAST ONE ADDED (35) WILL BE ON TOP OF THE ONES ADDED BEFORE (20 AND 10)
pile.push(99) #THIS WOULD BE THE FOURTH ELEMENT IN THE STACK, BUT IT WILL BE BELOW 35, 20, AND 10 BECAUSE 35 WAS ADDED BEFORE, THEN 20, THEN 10, REMEMBER THIS IS A LIFO STRUCTURE, SO THE LAST ONE ADDED (99) WILL BE ON TOP OF THE ONES ADDED BEFORE (35, 20, AND 10)

pile.print_stack() #HERE WE CALL THE PRINT FUNCTION AGAIN TO SEE WHAT IS CURRENTLY IN THE STACK, IT SHOULD SHOW THE VALUES IN LIFO ORDER (99 ON TOP, THEN 35, THEN 20, THEN 10)

print("\nPOPPING TWO ELEMENTS...\n")
print("REMOVED →", pile.pop())   # HERE WE CALL THE POP FUNCTION TO REMOVE THE TOP ELEMENT OF THE STACK, IT SHOULD RETURN 99 FIRST BECAUSE IT WAS THE LAST ONE ADDED (LIFO), THEN 35 ON THE SECOND CALL TO POP
print("REMOVED →", pile.pop())   # THE SECOND CALL TO POP SHOULD RETURN 35 BECAUSE IT WAS THE SECOND TO LAST ONE ADDED, THEN 20 AND 10 WILL STILL BE IN THE STACK

pile.print_stack() #HERE WE USE THE SAME LOGIC AS BEFORE TO PRINT THE CURRENT STATE OF THE STACK, IT SHOULD SHOW 20 ON TOP AND THEN 10 BELOW IT, BECAUSE 99 AND 35 WERE REMOVED WITH THE POP CALLS

print("\nPUSHING ONE MORE VALUE...\n")
pile.push(777) # THIS WOULD BE THE FIFTH ELEMENT IN THE STACK, BUT IT WILL BE BELOW 20 AND 10 BECAUSE 20 WAS ADDED BEFORE AND 10 WAS ADDED FIRST, REMEMBER THIS IS A LIFO STRUCTURE, SO THE LAST ONE ADDED (777) WILL BE ON TOP OF THE ONES ADDED BEFORE (20 AND 10)

pile.print_stack()

